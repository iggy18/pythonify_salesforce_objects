class LogoutEvent:

    def __init__(self, Id=None, CreatedDate=None, EventIdentifier=None, UserId=None, Username=None, EventDate=None, SessionKey=None, LoginKey=None, SessionLevel=None, SourceIp=None):
        self.Id = Id
        self.CreatedDate = CreatedDate
        self.EventIdentifier = EventIdentifier
        self.UserId = UserId
        self.Username = Username
        self.EventDate = EventDate
        self.SessionKey = SessionKey
        self.LoginKey = LoginKey
        self.SessionLevel = SessionLevel
        self.SourceIp = SourceIp


