class ContentNotification:

    def __init__(self, Id=None, Nature=None, UsersId=None, CreatedDate=None, EntityType=None, EntityIdentifierId=None, Subject=None, Text=None):
        self.Id = Id
        self.Nature = Nature
        self.UsersId = UsersId
        self.CreatedDate = CreatedDate
        self.EntityType = EntityType
        self.EntityIdentifierId = EntityIdentifierId
        self.Subject = Subject
        self.Text = Text


