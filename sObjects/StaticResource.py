class StaticResource:

    def __init__(self, Id=None, NamespacePrefix=None, Name=None, ContentType=None, BodyLength=None, Body=None, Description=None, CreatedDate=None, CreatedById=None, LastModifiedDate=None, LastModifiedById=None, SystemModstamp=None, CacheControl=None):
        self.Id = Id
        self.NamespacePrefix = NamespacePrefix
        self.Name = Name
        self.ContentType = ContentType
        self.BodyLength = BodyLength
        self.Body = Body
        self.Description = Description
        self.CreatedDate = CreatedDate
        self.CreatedById = CreatedById
        self.LastModifiedDate = LastModifiedDate
        self.LastModifiedById = LastModifiedById
        self.SystemModstamp = SystemModstamp
        self.CacheControl = CacheControl


